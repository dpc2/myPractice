public class HelloWorld {
// class holds data and behaviors

    public static void main(String[] args) {
//  public: other classes and methods outside of this have full access
//  static: main method belongs to HelloWorld class and can be called directly

//  main method: a special method that acts as the entry point of the application

        System.out.println("Hello World! This language has crazy syntax.");
        //  System is a class we have access to.
        //  out refers to the Standard Output Object, lets us send data to
        //  the Standard Output Stream (stdout)
        //  println is a method of the object out
        System.out.println("Hullo, Master Wayne!");
    }
}
